def area(a, b):
    '''
    Возвращает площадь прямоугольника.

	Параметры:
		a: длина прямоугольника
		b: ширина прямоугольника
	
	Возвращаемое значение:
		произведение a на b
    ''' 
    return a * b 

def perimeter(a, b):
    '''
    Возвращает периметр прямоугольника.

        Параметры:
                a: длина прямоугольника
                b: ширина прямоугольника

        Возвращаемое значение:
                удвоенная сумма a и b
    ''' 
    return 2*(a + b) 

