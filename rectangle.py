def area(a, b):
    '''Принимает 2 числа a и b - стороны прямоугольника. Возращает площадь прямоугольниа - произведение a и b''' 
    return a * b 

def perimeter(a, b): 
    '''Принимает 2 числа a и b - стороны прямоугольника. Возращает периметр прямоугольниа - удвоенна сумма a и b'''  
    return (a + b) * 2