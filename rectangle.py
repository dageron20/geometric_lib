def area(a, b):
    '''
    Приинимает два значение (а, б), где а и б - стороны прямоугольника
    Возвращает площадь прямоугольника
    '''
    return a * b

def perimeter(a, b):
    '''
    Приинимает два значение (а, б), где а и б - стороны прямоугольника
    Возвращает периметр прямоугольника
    '''
    return 2 * (a + b)
