def area(a, b):
    '''Возвращает площадь прямоугольника
    Входные данные:
        int a: сторона a, ширина
        int b: сторона b, длина
    Выходные данные:
        int c: (=a * b) Площадь
    '''
    return a * b

def perimeter(a, b):
    '''Возвращает периметр прямоугольника
    Входные данные:
        int a: сторона a, ширина
        int b: сторона b, длина
    Выходные данные:
        int c: (=a*2 + b*2) Периметр
    '''
    return a * 2 + b * 2
