def area(a, b):
    '''
    Принимает числа a, b - стороны прямоугольника,
    возвращает площадь прямоугольника, вычисленную по формуле
    '''
    return a * b 

def perimeter(a, b): 
    '''
    Принимает числа a, b - стороны прямоугольника,
    возвращает периметр прямоугольника, вычисленный по формуле
    '''
    return 2 * (a + b)
