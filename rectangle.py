def area(a, b): 
    '''
    Принимает a, b (int) - длины сторон прямоугольника
    Возвращает площадь этого прямоугольника
    '''
    return a * b 

def perimeter(a, b): 
    '''
    Принимает a, b (int) - длины сторон прямоугольника
    Возвращает периметр этого прямоугольника (int)
    '''
    return 2 * (a + b)
