def area(a, b): 
    '''Принимает на вход два числа a, b - стороны прямоугольника. Возвращает площадь этого прямоугольника.'''
    return a * b 

def perimeter(a, b): 
    '''Принимает на вход два числа a, b - стороны прямоугольника. Возвращает периметр этого прямоугольника.'''
    return (a + b) * 2 