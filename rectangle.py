def area(a, b):
    """
        Accepted parameters: (int) a, (int) b.
        Return value: the product of a and b - the area of the rectangle.
    """
    return a * b 

def perimeter(a, b):
    """
        Accepted parameters: (int) a, (int) b.
        Return value: the perimeter of the rectangle.
    """
    return (a + b) * 2