def area(a, b): 
    '''Принимает на вход числа a и b (стороны прямоугольника), выводит площадь прямоугольника с этими сторонами'''
    return a * b 

def perimeter(a, b): 
    '''Принимает на вход числа a и b (стороны прямоугольника), выводит периметр прямоугольника с этими сторонами'''
    return 2*(a + b )
