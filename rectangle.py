def area(a, b):
    '''Принимает 2 числа, a и b - стороны прямоугольника, возвращает площадь прямоугольника с заданными сторонами'''
    return a * b 

def perimeter(a, b):
    '''Принимает 2 числа, a и b - стороны прямоугольника, возвращает периметр прямоугольника с заданными сторонами'''
    return (a + b) * 2
