def area(a, b):
    '''
    параметры:
    a (int): первая сторона (число)
    b (int): вторая сторона (число)

    возвращаемое значение:
    площадь прямоугольника по формуле через две стороны
    '''
    return a * b

def perimeter(a, b):
    '''
    параметры:
    a (int): первая сторона (число)
    b (int): вторая стороны (число)

    возвращаемое значение:
    периметр прямоугольника по формуле через две стороны
    '''
    return (a + b) * 2