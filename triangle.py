def area(a, h):
    '''
    Возвращает площадь треугольника.

	Параметры:
		a: сторона
		h: высота к этой стороне

	Возвращаемое значение:
		полупроизведение a на h
    ''' 
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Возвращает периметр треугольника.

	Параметры:
		a: первая сторона
		b: вторая сторона
		c: третья сторона

	Возвращаемое значение:
		сумма a, b и c
    '''
    return a + b + c 

