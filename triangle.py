def area(a, h): 
    '''
    Возвращает площадь треугольника с заданной высотой и стороной.
        Параметры: 
            a (int): сторона треугольника
            h (int): высота треугольника
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''
    Возвращает периметр треугольника с заданными сторонами.
        Параметры:
            a (int): первая сторона
            b (int): вторая сторона
            c (int): третья сторона
    '''
    return a + b + c 
