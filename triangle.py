def area(a, h): 
    """Принимает два параметра a и h
    	 Расчитывает площадь треугольника по заданным параметрам"""

    return a * h / 2 

def perimeter(a, b, c):
    """Принимает параметры a, b, c
    	 Расчитывает периметр треугольника по заданным параметрам""" 
    return a + b + c 
