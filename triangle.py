def area(a, h): 
    '''Принимает 2 числа a и h - основание и высота треугольника. Возращает площадь треугольника - полупроизведение a и b''' 
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает 3 числа a,b,c - стороны треугольника. Возращает периметр треугольника - сумма a,b и c''' 
    return a + b + c 
