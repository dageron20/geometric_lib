def area(a, h):
    '''Вычисляет и возвращает площадь треугольника исходя из
        стороны(int a) и высоты(int h) опущенной к этой стороне'''
    return a * h / 2

def perimeter(a, b, c):
    '''Возвращает значение периметра треугольника исходя из
    трёх его сторон(int a, int b, int c)'''
    return a + b + c
