def area(a, h): 
    '''Возвращает площадь треугольника. 

        Параметры:
            a - длинна стороны треугольника.
            h - высота, проведенная к стороне a.

        Возвращаемое значение:
            Площадь треугольника по указанным параметрам.
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает три значения a, b, c - стороны треугольника. Возвращает периметр этого треугольника.'''
    return a + b + c 