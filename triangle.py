def area(a, h): 
    '''Принимает на вход числа a и h (сторону треугольника и его высоту, проведенную к этой стороне), возвращает площадь этого треугольника'''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает на вход три числа a, b, и c (3 стороны треугольника), возвращает периметр треугольника с этими сторонами'''
    return a + b + c 
