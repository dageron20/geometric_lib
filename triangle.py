def area(a,h):
    '''
    Принимает на вход сторону треугольника a и высоту, проведённую к этой стороне h
    возвращает площадь треугольника a*h/2
    '''
    return a*h/2

def perimeter(a, b, c):
    '''
    Принимает на вход сторонs треугольника a, b, c
    возвращает периметр треугольника a + b + c
    '''
    return a + b + c