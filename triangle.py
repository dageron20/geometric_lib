def area(a, h): 
    '''
    Принимает длину стороны и высоты треугольника 
    Возвращает площадь этого треугольника
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''
    Принимает длины трёх сторон треугольника
    Возвращает периметр этого треугольника
    '''
    return a + b + c
    