def area(a, h): 
    """
        Accepted parameters: side (int) a, height (int) h.
        Return value: half product of a and h - the area of the triangle.
    """
    return a * h / 2 

def perimeter(a, b, c):
    """
        Accepted parameters: side (int) a, side (int) b, side (int) c.
        Return value: the sum of a, b and c - the perimeter of the triangle.
    """
    return a + b + c 