def area(a, h):
    '''
    Принимает два значения. (а - основание треугольника, h - высота)
    Возвращает площадь треугольника
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
    Принимает три значения. (а, б, с - стороны треугольника)
    Возвращает периметр треугольника
    '''
    return a + b + c
