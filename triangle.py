def area(a, h):
    '''Принимает два числа a и h - высота и основание треугольника, возвращает его площадь по формуле a * h / 2'''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает три числа a, b, c - стороны треугольника, возвращает суму этих чисел(периметр тругольника)'''
    return a + b + c
