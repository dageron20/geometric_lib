def area(a, h):
    """принимает два числа: a - сторона треугольника, число h - высота, опущенная на сторону а. Возвращает площадь треугольника"""
    return a * h / 2

def perimeter(a, b, c):
    """Принимает числа a, b и c - стороны треугольника, возвращает периметр треугольника"""
    return a + b + c
