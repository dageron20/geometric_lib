def area(a, h): 
    '''
    Принимает числа a, h - сторона треугольника и высота,
    возвращает площадь треугольника, вычисленную по формуле
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''
    Принимает числа a, b, c - стороны треугольника,
    возвращает периметр треугольника, вычисленный по формуле
    '''
    return a + b + c
