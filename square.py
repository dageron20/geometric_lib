
def area(a):
    """
      Вычисляет площадь квадрата

      Параметр a - Радиус круга должен быть положительным.

      Возвращает площадь
    """
    return a * a


def perimeter(a):
    """
      Вычисляет периметр квадрата

      Параметр a - Сторона квадрата

      Возвращает периметр квадрата
    """
    return 4 * a
