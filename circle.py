import math


def area(r):
    '''Принимает на вход число r - радиус окружности. Возвращает площадь окружности с данным радиусом'''
    return math.pi * r * r


def perimeter(r):
    '''Принимает на вход число r - радиус окружности. Возвращает длинну окружности с данным радиусом'''
    return 2 * math.pi * r

