import math


def area(r):
    '''
    Принимает число r - радиус окружности,
    возвращает площадь круга, вычисленную по формуле
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Принимает число r - радиус окружности,
    возвращает периметр круга, вычисленный по формуле
    '''
    return 2 * math.pi * r

