class TriangleTests(unittest.TestCase):
    def test_triangle_area(self):
        for test in triangle_testcase_area:
            with self.subTest(test=test):
                if test[2] is not None:
                    self.assertEqual(triangle.area(test[0], test[1]), test[2])
                else:
                    with self.assertRaises(TypeError):
                        triangle.area(test[0], test[1])

    def test_triangle_perimeter(self):
        for test in triangle_testcase_perimeter:
            with self.subTest(test=test):
                if test[3] is not None:
                    self.assertEqual(triangle.perimeter(test[0], test[1], test[2]), test[3])
                else:
                    with self.assertRaises(TypeError):
                        triangle.perimeter(test[0], test[1], test[2])

